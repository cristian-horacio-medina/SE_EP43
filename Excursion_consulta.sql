JOIN GRADO ON EXCURSION.IdGRADO = GRADO.IdGRADO
JOIN alumnos ON EXCURSION.IdEXCURSION = alumnos.IdEXCURSION
WHERE EXCURSION.FECHA like '2024-10-24'

